<?php

namespace wms\declaration;

use Gcms\Login;
use Kotchasan\Html;
use Kotchasan\Http\Request;
use Kotchasan\Language;

class Controller extends \Gcms\Controller{
    public function render(request $request){

        $this-> title = Language::trans('{LNG_List of} {LNG_Declaration Report}');

        $section = Html::create('section',array(
         'class' => 'content_bg'
        ));
 
        $breadcrumbs = $section->add('div',array(
         'class' => 'breadcrumbs'
        ));
 
        $ul = $breadcrumbs->add('ul');
        $ul->appendChild('<li><a class="icon-fire" href="index.php">{LNG_Home}</a></li>');
        $ul->appendChild('<li><span>{LNG_Declaration}</span></li>');
 
        $section->add('header',array(
         'innerHTML'=>'<h4 class="icon-config">'.$this->title.'</h4>'
        ));
        
        $section->appendChild(\wms\declaration\View::create()->import($request));

        $section->appendChild(\wms\declaration\View::create()->render($request));
 
        return $section->render();

    }
}